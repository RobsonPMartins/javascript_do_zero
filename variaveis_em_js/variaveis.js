// Em JavaScript, as variáveis são usadas para armazenar dados.
// Você pode pensar nelas como caixas que contêm informações.

// Existem três maneiras de declarar variáveis em JavaScript:

// 1. Usando 'var':
// A palavra-chave 'var' era a forma tradicional de declarar variáveis.
var idade = 25; // Aqui, criamos uma variável chamada 'idade' e atribuímos o valor 25 a ela.

// 2. Usando 'let':
// A palavra-chave 'let' foi introduzida no ES6 (ECMAScript 2015) e é preferida para declarar variáveis.
let nome = "Alice"; // Aqui, criamos uma variável chamada 'nome' e atribuímos a string "Alice" a ela.

// 3. Usando 'const':
// 'const' é usado para criar variáveis constantes, cujo valor não pode ser alterado após a atribuição.
const pi = 3.14; // Aqui, criamos uma variável chamada 'pi' e atribuímos o valor 3.14 a ela.

// Tipos de dados:
// As variáveis podem armazenar diferentes tipos de dados, como números, strings, booleanos,
// objetos, arrays, funções e muito mais.

// Conversão de tipos de dados:
// JavaScript permite converter entre diferentes tipos de dados usando funções como 'parseInt',
// 'parseFloat' e 'String'.

