// Os operadores lógicos são usados para realizar operações lógicas entre expressões booleanas.
let p = true;
let q = false;

let and = p && q; // Operador "E" lógico: true se ambos são true, senão false
let or = p || q; // Operador "OU" lógico: true se pelo menos um for true, senão false
let not = !p; // Operador "NÃO" lógico: inverte o valor, true se p for false, e vice-versa

// Imprimindo os resultados
console.log("Operador 'E' lógico (AND):", and);
console.log("Operador 'OU' lógico (OR):", or);
console.log("Operador 'NÃO' lógico (NOT):", not);
