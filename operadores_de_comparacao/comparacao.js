// Os operadores de comparação são usados para comparar valores e retornar valores booleanos (true ou false).
let x = 10;
let y = 5;

let igual = x === y; // Igual a: false
let diferente = x !== y; // Diferente de: true
let maior = x > y; // Maior que: true
let menor = x < y; // Menor que: false
let maiorOuIgual = x >= y; // Maior ou igual a: true
let menorOuIgual = x <= y; // Menor ou igual a: false

// Imprimindo os resultados
console.log("Igual a:", igual);
console.log("Diferente de:", diferente);
console.log("Maior que:", maior);
console.log("Menor que:", menor);
console.log("Maior ou igual a:", maiorOuIgual);
console.log("Menor ou igual a:", menorOuIgual);
