// Definindo uma variável chamada 'contador' e atribuindo o valor 0.
let contador = 0;

// Usando uma estrutura de repetição 'while' para executar um bloco de código enquanto uma condição for verdadeira.
while (contador < 3) {
  // O código dentro deste bloco será executado enquanto a condição (contador < 3) for verdadeira.
  
  // Imprimindo o valor atual de 'contador' concatenado com a string "Contador: ".
  console.log("Contador: " + contador);

  // Incrementando o valor da variável 'contador' em 1.
  contador++;
}
