// Definindo uma variável chamada 'idade' e atribuindo o valor 18.
let idade = 18;

// Usando uma estrutura condicional 'if' para verificar se 'idade' é maior ou igual a 18.
if (idade >= 18) {
  // Se a condição for verdadeira (idade é maior ou igual a 18), este bloco será executado.
  console.log("Você é maior de idade.");
} else {
  // Se a condição for falsa (idade é menor que 18), este bloco será executado.
  console.log("Você é menor de idade.");
}
