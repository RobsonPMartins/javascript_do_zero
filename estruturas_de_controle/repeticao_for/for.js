// Usando uma estrutura de repetição 'for' para criar um loop com uma contagem de 0 a 4.
for (let i = 0; i < 5; i++) {
    // O código dentro deste bloco será executado repetidamente.
  
    // Imprimindo o valor atual de 'i' concatenado com a string "Número: ".
    console.log("Número: " + i);
  }
  