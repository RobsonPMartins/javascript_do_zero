// Usando um loop 'for' para criar uma contagem de 0 a 4.
for (let i = 0; i < 5; i++) {
    // O código dentro deste bloco será executado repetidamente enquanto a condição for verdadeira.
    
    // Verificando se 'i' é igual a 3.
    if (i === 3) {
      // Se a condição for verdadeira (quando 'i' é igual a 3), o 'break' é acionado.
      // O 'break' interrompe imediatamente a execução do loop.
      break; // Interrompe o loop quando 'i' for igual a 3
    }
    
    // Imprimindo o valor atual de 'i' concatenado com a string "Número: ".
    console.log("Número: " + i);
  }
  
