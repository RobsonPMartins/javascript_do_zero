// Definindo uma variável chamada 'numero' e atribuindo o valor 5.
let numero = 5;

// Usando uma estrutura de repetição 'do...while' para executar um bloco de código pelo menos uma vez.
do {
  // O código dentro deste bloco será executado pelo menos uma vez.
  console.log("Número: " + numero);

  // Decrementando o valor da variável 'numero' em 1.
  numero--;
} while (numero > 0);
