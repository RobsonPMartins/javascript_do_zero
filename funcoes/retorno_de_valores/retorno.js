// Declaração de função chamada 'somar' com dois parâmetros 'a' e 'b'.
function somar(a, b) {
    // Dentro da função, realizamos a operação de adição entre 'a' e 'b' e retornamos o resultado.
    return a + b;
  }
  
  // Chamando a função 'somar(3, 5)' e armazenando o resultado em 'resultado'.
  let resultado = somar(3, 5);
  
  // Imprimindo o valor armazenado em 'resultado', que é 8.
  console.log(resultado); // Imprimindo o resultado (8)
  