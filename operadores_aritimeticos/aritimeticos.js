// Os operadores aritméticos são usados para realizar operações matemáticas.
let a = 10;
let b = 5;

let soma = a + b; // Adição: 10 + 5 = 15
let subtracao = a - b; // Subtração: 10 - 5 = 5
let multiplicacao = a * b; // Multiplicação: 10 * 5 = 50
let divisao = a / b; // Divisão: 10 / 5 = 2
let resto = a % b; // Resto da divisão: 10 % 5 = 0

// Além disso, você pode usar parênteses para controlar a ordem de operações.
let resultado = (a + b) * 2; // (10 + 5) * 2 = 30

// Imprimindo os resultados
console.log("Resultado da Adição:", soma);
console.log("Resultado da Subtração:", subtracao);
console.log("Resultado da Multiplicação:", multiplicacao);
console.log("Resultado da Divisão:", divisao);
console.log("Resto da Divisão:", resto);
console.log("Resultado Final:", resultado);

console.log("--------------------------------------")